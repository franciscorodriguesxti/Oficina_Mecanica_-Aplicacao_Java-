    public void FazerConexao() {
        try {                       
            
            // Conectar ao Banco de Dados
            String host = "jdbc:mysql://localhost:3306/oficinamecanica?zeroDateTimeBehavior=convertToNull";
            String usuario = txtUsuario.getText();
            String senha = txtSenha.getText();
            
            con = DriverManager.getConnection(host, usuario, senha);
            btnEntrar.setEnabled(false); btnSair.setEnabled(true); btnNovaOS.setEnabled(true); btnConsultarOS.setEnabled(true);   
            txtSenha.setText(""); txtNumeroOS.setText(""); 
            txtUsuario.setEnabled(false); txtSenha.setEnabled(false); txtNumeroOS.setEnabled(true); lblNumeroOS.setEnabled(true);   
            
            // Coletar o Número da Última OS e mostrar na tela inicial
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);            
            String sql = "select max(osnum) from oficinamecanica.os;";
            rs = stmt.executeQuery(sql);
            rs.next();
            String ultimaos = rs.getString(1);
            if ("0".equals(ultimaos)) { lblUltimaOS.setText("0"); } else { lblUltimaOS.setText(ultimaos); }
            
        }
        catch (SQLException err) {
            JOptionPane.showMessageDialog(OrdemDeServico_Testes.this, err.getMessage());
        }
    }
